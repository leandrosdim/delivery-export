const XLSX = require("xlsx");
const path = require("path");
const fs = require("fs");

const INPUT_FILE = path.resolve(__dirname, "../data/IMPORT.xlsx");
const OUTPUT_FILE = path.resolve(__dirname, "../src/data/dropdownOptions.js");

const SHEET_CONFIG = [
  { sheetName: "\u0391\u03a0\u039f\u03a3\u03a4\u039f\u039b\u0397", fieldKey: "apostoli" },
  { sheetName: "\u03a3\u03a5\u039d\u0398\u0397\u039a\u0397 \u039c\u0395\u03a4\u0391\u03a6\u039f\u03a1\u0391\u03a3", fieldKey: "synthiki" },
  { sheetName: "\u03a0\u0391\u039b\u0395\u03a4\u0391 \u0389 \u039a\u0399\u0392\u03a9\u03a4\u0399\u039f", fieldKey: "pal_kib" },
  { sheetName: "\u03a0\u039b\u0397\u03a1\u03a9\u03a4\u0397\u03a3", fieldKey: "plirotis" },
];

const FIELD_OPTIONS_SOURCE_TO_KEY = {
  "\u0391\u03a0\u039f\u03a3\u03a4\u039f\u039b\u0397": "apostoli",
  "\u03a3\u03a5\u039d\u0398\u0397\u039a\u0397 \u039c\u0395\u03a4\u0391\u03a6\u039f\u03a1\u0391\u03a3": "synthiki",
  "\u03a0\u0391\u039b\u0395\u03a4\u0391 \u0389 \u039a\u0399\u0392\u03a9\u03a4\u0399\u039f": "pal_kib",
  "\u03a0\u039b\u0397\u03a1\u03a9\u03a4\u0397\u03a3": "plirotis",
};

function extractOptions(sheetName, sheet) {
  const data = XLSX.utils.sheet_to_json(sheet, { header: 1, defval: "" });
  const nonEmptyRows = data.filter((row) =>
    row.some((cell) => String(cell).trim() !== "")
  );

  if (nonEmptyRows.length === 0) return [];

  const firstRow = nonEmptyRows[0];
  const hasMultipleColumns =
    firstRow.length >= 2 && String(firstRow[1]).trim() !== "";

  if (hasMultipleColumns) {
    return nonEmptyRows
      .filter((row) => String(row[0]).trim() !== "")
      .map((row) => ({
        value: String(row[0]).trim(),
        label: String(row[1]).trim(),
      }));
  }

  const hasTitleRow =
    nonEmptyRows.length > 1 &&
    String(firstRow[0]).trim().toUpperCase() === sheetName.trim().toUpperCase();

  const startIdx = hasTitleRow ? 1 : 0;

  return nonEmptyRows
    .slice(startIdx)
    .filter((row) => String(row[0]).trim() !== "")
    .map((row) => ({
      value: String(row[0]).trim(),
      label: String(row[0]).trim(),
    }));
}

function main() {
  if (!fs.existsSync(INPUT_FILE)) {
    console.error(`Input file not found: ${INPUT_FILE}`);
    process.exit(1);
  }

  const workbook = XLSX.readFile(INPUT_FILE);
  const allOptions = {};

  for (const { sheetName, fieldKey } of SHEET_CONFIG) {
    const sheet = workbook.Sheets[sheetName];
    if (!sheet) {
      console.error(`Sheet "${sheetName}" not found. Available: ${workbook.SheetNames.join(", ")}`);
      process.exit(1);
    }

    const options = extractOptions(sheetName, sheet);
    allOptions[fieldKey] = options;
    console.log(`\n${sheetName} (${fieldKey}): ${options.length} options`);
    options.slice(0, 3).forEach((opt) => {
      console.log(`  ${JSON.stringify(opt)}`);
    });
    if (options.length > 3) {
      console.log(`  ... and ${options.length - 3} more`);
    }
  }

  const outputLines = [];
  outputLines.push("// Auto-generated by scripts/extract-dropdown-options.js");
  outputLines.push("// Do not edit manually. Run: npm run extract:dropdowns");
  outputLines.push("");
  outputLines.push("export const DROPDOWN_OPTIONS = {");

  for (const { fieldKey } of SHEET_CONFIG) {
    const options = allOptions[fieldKey];
    outputLines.push(`  ${fieldKey}: [`);
    for (const opt of options) {
      outputLines.push(`    { value: ${JSON.stringify(opt.value)}, label: ${JSON.stringify(opt.label)} },`);
    }
    outputLines.push("  ],");
  }

  outputLines.push("};");
  outputLines.push("");

  outputLines.push("export const FIELD_OPTIONS_SOURCE_TO_KEY = {");
  for (const [sheetName, fieldKey] of Object.entries(FIELD_OPTIONS_SOURCE_TO_KEY)) {
    outputLines.push(`  ${JSON.stringify(sheetName)}: ${JSON.stringify(fieldKey)},`);
  }
  outputLines.push("};");
  outputLines.push("");

  outputLines.push("export function getDropdownOptions(fieldKey) {");
  outputLines.push("  return DROPDOWN_OPTIONS[fieldKey] || [];");
  outputLines.push("}");
  outputLines.push("");

  outputLines.push("export function hasDropdownOptions(fieldKey) {");
  outputLines.push("  return fieldKey in DROPDOWN_OPTIONS && DROPDOWN_OPTIONS[fieldKey].length > 0;");
  outputLines.push("}");

  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, outputLines.join("\n") + "\n", "utf8");
  console.log(`\nWritten to: ${OUTPUT_FILE}`);
}

main();